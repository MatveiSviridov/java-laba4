package com.laba4;

public class Division {
    private int id;
    private String name;

    /**
     * Setter for id
     * @param newId setting Id
     */
    public void setId(int newId){
        id = newId;
    }
    /**
     * Getter for id
     * @return divisions's id
     */
    public int getId(){
        return id;
    }
    /**
     * Setter for name
     * @param newName setting name
     */
    public void setName(String newName){
        name = newName;
    }
    /**
     * Getter for name
     * @return divisions's name
     */
    public String getName(){
        return name;
    }




}
